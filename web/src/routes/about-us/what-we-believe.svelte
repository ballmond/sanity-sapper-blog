<h3><strong>Ten Facts You Should Know About American Baptists</strong></h3>
<p>
    The 1.3-million members and 5,200 congregations of American Baptist Churches USA share with more than 42 million Baptists around the world a common tradition begun in the early 17th century. That tradition has emphasized the Lordship and atoning sacrifice of Jesus Christ, believers’ baptism, the competency of all believers to be in direct relationship with God and to interpret Scripture, the importance of the local church, the assurance of freedom in worship and opinion, and the need to be Christ’s witnesses within society.
    <br>
    <br>
    The following facts are representative of the tradition and practice of American Baptists.
</p>
<ul>
    <li>American Baptists believe that Jesus Christ is Lord and Savior, and that the Bible is the divinely inspired word of God that serves as the final written authority for living out the Christian faith.</li>
    <li>For American Baptists the local church is the fundamental unit of mission in denominational life.</li>
    <li>American Baptists partake of two ordinances: believers’ baptism and The Lord’s Supper.</li>
    <li>American Baptists believe that the committed individual Christian can and should approach God directly, and that individual gifts of ministry should be shared.</li>
    <li>American Baptists take seriously the call to evangelism and missionary work.</li>
    <li>American Baptists support religious freedom and respect the expressions of faith of others.</li>
    <li>American Baptists acknowledge that God’s family extends beyond our local churches, and that God calls us to  cooperative  ministries.</li>
    <li>American Baptists have been called to be Christ’s witnesses for justice and wholeness within a broken society.</li>
    <li>American Baptist Churches USA celebrates the racial, cultural and theological diversity witnessed within its membership.</li>
    <li>American Baptists heed the biblical call to renewal and the need for a vital witness in a new millennium.</li>
</ul>
<h3><strong><em>Facts you should know about Grace Baptist Church</em></strong></h3>
<ul>
    <li>Grace Baptist Church, founded in 1872, is an American Baptist Church, and currently, we are members of the Philadelphia Baptist Association (PBA), which is the oldest continuous association of Baptist churches in the United States.</li>
    <li>Our congregation was originally located in Philadelphia. Its original name was also Grace Baptist Church. When the church started to grow, and they erected a new church building at Broad and Berks Streets, it took on the nickname “The Temple.”</li>
    <li>Hattie May Wiatt was a small girl who saved up 57 cents for the building of the new church facility. She sadly passed away in 1886       before the land was purchased, but Dr. Conwell used her donation to help raise the other necessary funds. The full story is on our website- gracebaptistbluebell.org. It has been retold for many years in many congregations across the country.</li>
    <li>Dr. Russell Conwell, who was pastor of the church at the time, oversaw the building of the new, much larger church building, and also facilitated the founding of The Temple University (which started with 7 individuals who sought a higher education, but did not have the money to pay for it), as well as the founding of Temple Hospital (the small Samaritan, Garretson, and Greatheart hospitals were merged to form one larger hospital).</li>
    <li>The congregation moved out to Blue Bell in 1975.</li>
</ul>
